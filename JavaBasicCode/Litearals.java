public class Litearals {
    public static void main(String[] args) {
        // Integer literal
        int intLiteral = 100;
        
        // Floating-point literal
        double floatLiteral = 10.5;
        
        // Character literal
        char charLiteral = 'A';
        
        // String literal
        String stringLiteral = "Hello, World!";
        
        // Boolean literal
        boolean boolLiteral = true;
        
        // Print all literals
        System.out.println("Integer Literal: " + intLiteral);
        System.out.println("Floating-point Literal: " + floatLiteral);
        System.out.println("Character Literal: " + charLiteral);
        System.out.println("String Literal: " + stringLiteral);
        System.out.println("Boolean Literal: " + boolLiteral);
    }
    
}
