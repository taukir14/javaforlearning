// BasicOutput.java
// Demonstrates different output methods in Java
//it shows the difference between println, print, and printf
//its similar like PrintVariables.java but focuses on output methods
//its similar like cout,printf in C/C++

public class BasicOutput {    
    public static void main(String[] args) {
int d = 10;
        System.out.println("1. println "); // moves to the next line
        System.out.println("2. println "); // moves to the next line
        System.out.print("1. print "); // stays on the same line
        System.out.printf("2. printf %d", d); // stays on the same line

    } 
    
}
